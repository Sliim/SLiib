namespace SLiib\WebApp;

/**
 * Test class for \SLiib\WebApp\Security.
 * Generated by PHPUnit on 2011-10-17 at 23:57:42.
 *
 * @package    Tests
 * @subpackage UnitTests
 */
class SecurityTest extends \PHPUnit_Framework_TestCase
{


    /**
     * Test check
     *
     * @covers \SLiib\WebApp\Security::check
     *
     * @return void
     */
    public function testCheck()
    {
        $mock = $this->getMockForAbstractClass('\Stubs\Security\Model', array('Positive'));
        Request::init();
        Security::check(array($mock));

    }


    /**
     * Test check invalid checkers
     *
     * @covers \SLiib\WebApp\Security::check
     *
     * @return void
     */
    public function testCheckInvalidChecker()
    {
        try {
            Security::check(array('w00t'));
        } catch (Security\Exception\CheckerError $e) {
            $this->assertInstanceOf('\SLiib\WebApp\Security\Exception\CheckerError', $e);
            return;
        } catch (\Exception $e) {
            $this->fail('Bad exception has been raised');
        }

        $this->fail('No exception has been raised');

    }


}

