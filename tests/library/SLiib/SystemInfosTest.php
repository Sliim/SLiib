namespace SLiib;

/**
 * Test class for \SLiib\SystemInfos.
 * Generated by PHPUnit on 2011-10-14 at 20:56:19.
 *
 * @package    Tests
 * @subpackage UnitTests
 */
class SystemInfosTest extends \PHPUnit_Framework_TestCase
{


    /**
     * Appel commande Apache2
     * apache2 on debian testing fail with normal user (not /usr/sbin in his $PATH)
     *
     * @return void
     */
    public function testCmdApache2()
    {
        try {
            $res = SystemInfos::CMD_APACHE2_VERSION('serialize');
            $this->assertInternalType('string', $res);
            $res = SystemInfos::CMD_APACHE2_COMPILED_MODULES();
            $this->assertInternalType('string', $res);
        } catch (SystemInfos\Exception\CommandFailed $e) {
            $this->assertInstanceOf('\SLiib\SystemInfos\Exception\CommandFailed', $e);
            return;
        } catch (\Exception $e) {
            $this->fail('Bad exception has been raised');
        }

    }


    /**
     * Appel commande PHP
     *
     * @return void
     */
    public function testCmdPHP()
    {
        $res = SystemInfos::CMD_PHP_VERSION();
        $this->assertInternalType('string', $res);

        $res = SystemInfos::CMD_PHP_MODULES();
        $this->assertInternalType('string', $res);

    }


    /**
     * Appel commande Uname
     *
     * @return void
     */
    public function testCmdUname()
    {
        $res = SystemInfos::CMD_UNAME_KERNEL_RELEASE();
        $this->assertInternalType('string', $res);

        $res = SystemInfos::CMD_UNAME_OS_INFOS('serialize');
        $this->assertInternalType('string', $res);

    }


    /**
     * Appel commande LSBRelease
     *
     * @return void
     */
    public function testCmdLsbRelease()
    {
        $res = SystemInfos::CMD_LSB_RELEASE_CODENAME();
        $this->assertInternalType('string', $res);

        $res = SystemInfos::CMD_LSB_RELEASE_RELEASE();
        $this->assertInternalType('string', $res);

    }


    /**
     * Test command unknown
     *
     * @return void
     */
    public function testCmdUnknown()
    {
        $this->setExpectedException('\SLiib\SystemInfos\Exception\BadMethodCall');
        $res = SystemInfos::CMD_UNKNOWN();

    }


    /**
     * Test command failed
     *
     * @return void
     */
    public function testCmdFailed()
    {
        $this->setExpectedException('\SLiib\SystemInfos\Exception\CommandFailed');
        $res = \Stubs\SystemInfos::CMD_FAILED();

    }


}