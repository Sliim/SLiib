require_once 'PHPUnit/Framework.php';
require_once 'SLiib/Application.php';

define('ROOT_PATH', realpath(dirname(__FILE__) . '/../../others/SecurityTests/'));
define('APP_PATH', realpath(ROOT_PATH . '/application/'));
define('APP_NS', 'SecurityTests');

/**
 * Test class for SLiib_Security*.
 * Generated by PHPUnit on 2011-10-17 at 23:57:42.
 *
 * @package    SLiib
 * @subpackage UnitTests
 */
class SLiib_SecurityTest extends PHPUnit_Framework_TestCase
{


    const ENABLE_OUTPUT_CLEANER = FALSE;

    /**
     * @var string
     */
    private $_ip = '127.0.0.1';

    /**
     * @var string
     */
    private $_method = 'GET';

    /**
     * @var string
     */
    private $_userAgent = 'Units Test SLiib_Application';

    /**
     * @var string
     */
    private $_referer = 'http://dtc.com';

    /**
     * @var SLiib_HTTP_Request
     */
    private $_request;


    /**
     * Sets up the fixture, for example, opens a network connection.
     * This method is called before a test is executed.
     *
     * @return void
     */
    public function setUp()
    {
        $this->_setServerInfo('REMOTE_ADDR', $this->_ip);
        $this->_setServerInfo('REQUEST_METHOD', $this->_method);
        $this->_setServerInfo('HTTP_USER_AGENT', $this->_userAgent);
        $this->_setServerInfo('HTTP_REFERER', $this->_referer);

    }


    /**
     * Test run app with error on boostrap
     *
     * @return void
     */
    public function testRunAppBootstrapError()
    {
        $GLOBALS['testBoostrapError'] = TRUE;
        try {
            $this->_runApp();
        } catch (SLiib_Security_Exception_CheckerError $e) {
            $GLOBALS['testBoostrapError'] = FALSE;
            $this->assertType('SLiib_Security_Exception_CheckerError', $e);
            return;
        } catch (Exception $e) {
            $GLOBALS['testBoostrapError'] = FALSE;
            $this->fail('Bad Exception has been raised');
        }

        $GLOBALS['testBoostrapError'] = FALSE;
        $this->fail('No exception has been raised');

    }


    /**
     * Run application
     *
     * @return void
     */
    private function _runApp()
    {
        if (self::ENABLE_OUTPUT_CLEANER) {
            ob_start();
        }

        SLiib_Application::init(
            APP_NS,
            APP_PATH
        )->run();

        if (self::ENABLE_OUTPUT_CLEANER) {
            ob_clean();
            ob_flush();
        }

        $this->_request = SLiib_HTTP_Request::getInstance();

    }


    /**
     * Set index of $_SERVER for tests
     *
     * @param string $index Index of $_SERVER
     * @param string $value Value to assign
     *
     * @return void
     */
    private function _setServerInfo($index, $value)
    {
        $GLOBALS['_SERVER'];
        $_SERVER[$index] = $value;

    }


    /**
     * Simulate a $_POST
     *
     * @param array $post $_POST you want
     *
     * @return void
     */
    private function _setPost(array $post)
    {
        $GLOBALS['_POST'];
        $_POST = $post;

    }


    /**
     * Simulate a $_COOKIE
     *
     * @param array $cookies $_COOKIE you want
     *
     * @return void
     */
    private function _setCookie(array $cookies)
    {
        $GLOBALS['_COOKIE'];
        $_COOKIE = $cookies;

    }


}
