/**
 * Test class for SLiib_Autoloader.
 * Generated by PHPUnit on 2011-11-22 at 20:10:12.
 *
 * @package    SLiib
 * @subpackage UnitTests
 */
class SLiib_AutoloaderTest extends PHPUnit_Framework_TestCase
{


    /**
     * Test init autolodaer
     *
     * @covers SLiib_Autoloader::init
     *
     * @return void
     */
    public function testInit()
    {
        $namespace = array('SLiib' => 'SLiib', APP_NS => APP_PATH);
        $section = array(APP_NS => array('Model' => 'models'));
        SLiib_Autoloader::init($namespace, $section);

        $newSection = array(
                       APP_NS => array('Controller' => 'controllers'),
                       'Test2' => 'foo',
        );
        SLiib_Autoloader::init($namespace, $newSection);

    }


    /**
     * Test autoload
     *
     * @covers SLiib_Autoloader::autoload
     * @covers SLiib_Autoloader::_searchForInclude
     *
     * @return void
     */
    public function testAutoload()
    {
        $res = SLiib_Autoloader::autoload('SLiib_Listing');
        $this->assertTrue($res);

        $res = SLiib_Autoloader::autoload('SLiib_Listing');
        $this->assertTrue($res);

        $res = SLiib_Autoloader::autoload('SLiib');
        $this->assertFalse($res);

        $res = SLiib_Autoloader::autoload('SLiip_Listing');
        $this->assertFalse($res);

        $res = SLiib_Autoloader::autoload('Test_Model_Foo');
        $this->assertFalse($res);

        $res = SLiib_Autoloader::autoload('Test_Model_MyModel');
        $this->assertTrue($res);

        $res = SLiib_Autoloader::autoload('SLiib_NotExist');
        $this->assertFalse($res);

    }


}
